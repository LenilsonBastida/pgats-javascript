/*
  Conceitos Básicos de Node.js e npm
  Node.js e npm
    Node.js é um ambiente de execução JavaScript do lado do servidor, que permite executar código JavaScript fora de um navegador. 
    Ele é construído sobre o motor V8 do Google Chrome e é amplamente utilizado para criar aplicações web escaláveis e de alto desempenho.
    O Node Package Manager (npm) é o gerenciador de pacotes padrão para o Node.js, que permite instalar, compartilhar e gerenciar dependências de projetos JavaScript. 
    Ele facilita a instalação de bibliotecas e ferramentas necessárias para o desenvolvimento de aplicações Node.js.

  Node
    v8 - motor de execução de código do JavaScript FORA DO NAVEGADOR
    npm - gerenciador de pacotes do Node.js, utilizado para instalar bibliotecas e dependências
  
  NPM (Node Package Manager)
    - npm init -y - cria um package.json com as configurações padrão
    - npm install <nome-do-pacote> - instala um pacote específico
    - npm install <nome-do-pacote>@<versão> - instala uma versão específica de um pacote
    - npm uninstall <nome-do-pacote> - remove um pacote instalado
    - npm update <nome-do-pacote> - atualiza um pacote instalado para a versão mais recente
    - npm list - lista todos os pacotes instalados no projeto
    - npm run <script> - executa um script definido no package.json
    - npx <comando> - executa um comando de um pacote instalado sem precisar instalá-lo globalmente

*/